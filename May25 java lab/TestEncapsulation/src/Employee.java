
public class Employee {
  private String name;
private int age;
private double salary;

    public String getName() {
        return name;
    }

    public void setName(String N) {
        this.name = N;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int A) {
        this.age = A;
    }

    public double getSalary() {
        return salary;
    }

    public void setSalary(double S) {
        this.salary = S;
    }

}
